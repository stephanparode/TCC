                    <h1>O que é o Planejaí?</h1>
                </div>
                <div class="texto">
                    <p>Entre os diversos obstáculos enfrentados pela população brasileira em sua trajetória acadêmica, 
                        situa-se uma dificuldade generalizada relativa ao ingresso às universidades, especialmente para 
                        estudantes de escola pública. Nesse contexto, o Planejaí surge como alternativa viável para colaborar
                        com a universalização do acesso ao ensino superior, sobretudo por ser um serviço completamente gratuito.
                        Desenvolvido e implementado pelo estudante Stéphan Coelho Parode como trabalho de conclusão do curso
                        técnico em informática integrado ao ensino médio da Escola Maria Rocha, o Planejaí é um site de assistência a estudantes
                        e planejamento de estudo para vestibulares, direcionando seu foco ao ENEM de maneira especial.
                    </p>
                    <p>Temos como intuito enfatizar a importância da organização e do planejamento em toda e qualquer
                        trajetória, incluindo os estudos. O Planejaí propõe, por meio da internet, contribuir com seus estudos
                        oferecendo instruções e ferramentas fundamentais para o êxito nos vestibulares, possibilitando conhecer
                        o método de estudo mais adequado para seu estilo. 
                    </p>
                    <h4>"Comece por arrumar a tua própria vida; com pequenos passos, tudo se encaixa."</h4>
                </div>
            </div>


            <div class="blocotexto" id="tcc">
                <div class="titulo">
                    <div class="imagem">
                        <img src="images/concentrado.png" alt="personagem concentrado">
                    </div>
                    <h1>TCC e Trabalho Teórico</h1>
                </div>
                <div class="texto">
                    <p>Antes de utilizarmos todas as funcionalidades que o Planejaí disponibiliza, é de suma importância entender
                        as motivações por trás do site, incluindo sua fundamentação teórica e o trabalho de pesquisa dos bastidores.
                    </p>
                    <p>Nossa tese reconhece uma notória desigualdade nas condições econômicas, culturais e sociais de dois grupos de estudantes,
                        os quais chamamos de "estudante herdeiro" e "novo estudante", caracterizados por uma vasta herança de capital
                        cultural e financeiro e um alheamento econômico e cultural, respectivamente. Para além das desigualdades práticas, cujas origens também analisamos
                        de forma pragmática, identificamos uma problemática elementar na construção desse cenário: por uma diversidade de fatores, o novo estudante,
                        de escola pública e habitualmente de baixa renda, sente-se perdido em um mundo ao qual jamais foi introduzido. Desse modo, além de sofrer com a 
                        precariedade estrutural, torna-se expressa uma negligência cultural à grande parte da população, que possui um irrisório acesso à cultura e à informação, e
                        é essa lacuna que tomamos a liberdade de preencher. Para tanto, buscamos auxiliar todo e qualquer estudante a compreender o cenário educacional do país,
                        de modo a capacitá-lo para o acesso ao ensino superior com uma metodologia que busca fidelidade ao desígnio primordial dos vestibulares — Avaliar a capacidade
                        crítica e cognitiva dos estudantes. Esses dois elementos, dignos de atenção, não podem ser desenvolvidos somente pela metódica rotina
                        tradicional do vestibulando, mas com planejamento centrado em uma trajetória robusta e abrangente, que busque sobretudo remediar inclusive os impasses
                        oriundos de fenômenos que permeiam muito além das escolas. Entre eles, destacam-se as limitações de renda, e aqui reside a motivação por trás da criação de um site
                        totalmente gratuito. Além disso, o Planejaí busca disponibilizar material informativo no que concerne à educação e cultura, de modo a situar na sociedade
                        sujeitos de qualquer ponto de partida.
                    </p>
                    <p>Fundamentada principalmente por Bourdieu, Adorno, Horckeimer e Elrich, o trabalho teórico busca contemplar as raízes 
                        de um desafio que, se não observado com a devida atenção, toma caráter quase espectral, intangível. É evidente que somente o esforço não basta e que, portanto,
                        o auxílio no planejamento é substancial, por isso nosso enfoque no poder da organização, que só se torna possível com o esclarecimento. Em síntese, o Planejaí busca
                        pavimentar o caminho para o crescimento intelectual do estudante, e enfim aproximá-lo do tão sonhado acesso ao ensino superior.
                    </p>

                    <h2>Síntese do TCC em tópicos</h2>
                    <div class="topicos-tcc">
                        <div class="topico">
                            <h3>Desigualdade entre estudantes</h3>
                            <p>O estudo evidencia a diferença entre dois grupos de estudantes: o "estudante herdeiro", que possui ampla
                                herança cultural e financeira, e o "novo estudante", geralmente oriundo de escola pública e baixa renda,
                                sem acesso às mesmas oportunidades culturais e educacionais. Essa distinção é fundamental para entender
                                as barreiras que dificultam a inserção desses estudantes no ensino superior.
                            </p>
                        </div>
                        <div class="topico">
                            <h3>Desafios do novo estudante</h3>
                            <p>O novo estudante enfrenta não apenas limitações materiais, mas também lacunas culturais que o afastam de
                                ambientes acadêmicos mais sofisticados. Sente-se perdido em um mundo desconhecido, evidenciando a necessidade
                                de apoio estruturado para reduzir o impacto dessas desigualdades na trajetória educacional.
                            </p>
                        </div>
                      <div class="topico">
                            <h3>Dificuldades no acesso à cultura</h3>
                            <p>O acesso limitado a livros, museus e outras formas de cultura dificulta a formação do pensamento crítico e da capacidade de análise dos estudantes. Essa carência restringe a construção de repertório e prejudica habilidades importantes para interpretação e argumentação, essenciais para o ENEM e vestibulares.</p>
                      </div>

                        <div class="topico">
                            <h3>Desigualdade reforçada pela privatização</h3>
                            <p>A privatização da educação favorece estudantes de maior renda, que têm acesso a cursos preparatórios e materiais exclusivos, perpetuando desigualdades estruturais. Estudantes de baixa renda enfrentam barreiras financeiras e estruturais, tornando difícil competir em igualdade de condições.</p>
                        </div>

                        <div class="topico">
                            <h3>Desafios pessoais e sociais</h3>
                            <p>Estudantes de escolas públicas frequentemente precisam conciliar estudos com trabalho ou responsabilidades familiares, enfrentando falta de incentivo e marginalização histórica. Esses fatores tornam a rotina de estudo mais difícil e aumentam a desigualdade frente a estudantes de escolas privadas.</p>
                        </div>


                        <div class="topico">
                            <h3>Objetivo do Planejaí</h3>
                            <p>O Planejaí foi desenvolvido para auxiliar estudantes de diferentes contextos sociais a compreender
                                o cenário educacional e organizar seus estudos. A plataforma gratuita fornece informações e ferramentas
                                que possibilitam o planejamento de estudos para o ENEM e outros vestibulares, promovendo maior autonomia e
                                consciência do próprio processo de aprendizagem.
                            </p>
                        </div>
                        <div class="topico">
                            <h3>Metodologia de ensino</h3>
                            <p>A plataforma prioriza o desenvolvimento da capacidade crítica e cognitiva, alinhada aos objetivos dos
                                vestibulares. Isso é feito por meio de planejamento estruturado e estratégias de estudo que contemplam
                                não apenas o conteúdo programático, mas também as competências exigidas, garantindo que o estudante se
                                prepare de forma completa e consciente.
                            </p>
                        </div>
                        <div class="topico">
                            <h3>Fundamentação teórica</h3>
                            <p>O trabalho se apoia em autores como Bourdieu, Adorno, Horckeimer e Elrich, que fornecem suporte teórico
                                para compreender as raízes das desigualdades educacionais e culturais. A análise teórica fundamenta a
                                necessidade de intervenções como o Planejaí para reduzir lacunas de acesso à educação.
                            </p>
                        </div>
                    </div>
                    <h4>"Tudo o que pretendes nasce primeiro em teu pensamento; organiza a mente e o caminho adiante se tornará claro."</h4>
                </div>
